# 1. ユーザー入力を受け取り、計算ができるように整数型(int)に変換します
# input()は常に「文字列」を返すため、int()での変換が必須です [569, 会話履歴]
x = int(input("type a number: "))
y = int(input("type a number: "))

# 2. 関数 f の定義 (引数を2で割った整数の商を返す)
# 以前のエラーのように定義行(def)に計算式を書かず、内部で処理します [会話履歴]
def f(val):
    return val // 2

# 3. 関数 g の定義 (引数を4倍にして返す)
def g(val):
    return val * 4

# 4. 関数の呼び出しと結果の出力
# 関数名に括弧 () を付けて実行し、引数として変数(x, y)を渡します [640, 会話履歴]
# print(f) とだけ書くとメモリ上の参照情報が表示されるため、f(x) と記述します [会話履歴]
print(f(x))
print(g(y))
